function shuffle<T>(arr: T[]): T[] {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

function rand(min: number, max: number) {
  return Math.floor(Math.random() * (max - min + 1)) + min
}

function makeChoices(answer: number, variants: number[]): string[] {
  const pool = new Set([answer, ...variants])
  while (pool.size < 4) pool.add(rand(Math.max(1, answer - 10), answer + 10))
  const arr = shuffle([...pool]).slice(0, 4)
  if (!arr.includes(answer)) arr[0] = answer
  return shuffle(arr).map(String)
}

